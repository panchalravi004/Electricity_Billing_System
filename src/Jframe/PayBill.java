    static int payAmt;
    public void setPayAmtDetail()
    {
        final int RATE = 10;
        final int GST_RATE = 9;
        int rowNo = c_table.getSelectedRow();
        model = (DefaultTableModel) c_table.getModel();
        
        txt_pay_meter.setText(model.getValueAt(rowNo,0).toString());
        txt_pay_unit.setText(model.getValueAt(rowNo,7).toString());
        
        int unit = Integer.parseInt(txt_pay_unit.getText());
        int bgst = unit * RATE;
        int gst = (bgst * GST_RATE)/100;
        payAmt = bgst + gst;
        
        txt_pay_amt.setText(String.valueOf(payAmt));
    }
