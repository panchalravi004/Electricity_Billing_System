    public void setTable()
    {
        String find_cos = txt_search.getText();
        String find_year = (String) combo_year.getSelectedItem();
        String find_month = (String) combo_month.getSelectedItem();
        jScrollPane1.getVerticalScrollBar().setPreferredSize(new Dimension(0, 0));
        try
        {
            Connection con = DBconnection.getConnection();
            String sql = "select * from customer where meterno=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(find_cos));
            ResultSet rs = pst.executeQuery();
            if(rs.next())
            {
                String sql1 = "select * from unit_data where meterno=? and year=? and month=?";
                PreparedStatement pst1 = con.prepareStatement(sql1);
                pst1.setInt(1, Integer.parseInt(find_cos));
                pst1.setString(2, find_year);
                pst1.setString(3, find_month);
                
                ResultSet rs1 = pst1.executeQuery();
                if(rs1.next())
                {
                    meterno = rs.getInt(1);
                    fname = rs.getString(2);
                    lname = rs.getString(3);
                    district = rs.getString(4);
                    city = rs.getString(5);
                    mobile = rs.getString(6);
                    pincode = rs.getString(7);
                    unit = rs1.getInt(4);
                    year = rs1.getString(2);
                    month = rs1.getString(3);
                    
                    Object[] obj = {meterno,fname,lname,district,city,mobile,pincode,unit};
                
                    model = (DefaultTableModel) c_table.getModel();
                    model.addRow(obj);
                   
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "There is No Data of Month "+find_month);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "There is No Customer Like This "+find_cos);
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
